# Append
names = ["Manas", "Ashwani", "Aditi", "BulBul", "Mickey", "Minal"]

names.append("Acer")
print(names)

# Insert
names.insert(2,"cute")
print(names)

# Remove
names.remove("Manas")
print(names)

# POP
names.pop(3)
print(names)

# Sort
names.sort()
print(names)

# Reverse
names.reverse()
print(names)