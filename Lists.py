# Lists in python

food = ["Pizza", "Burger", "Rolls", "Noodles", "Waffles", "Cake"]
print(food)
print(len(food))
print("My fav food is: ", food[5])
print("My least fav food is: ", food[2])

print(type(food))            #datatype
print(food.count("Pizza"))   #count
print(food.index("Rolls"))               #indexing